/**
 * GPX Route Generator — APEX Coach Center
 * Generates geometric .gpx routes based on location + training prescription.
 * Routes are geometric (not road-following) — load into Garmin/Wahoo as a guide.
 */

// Average speed by sport and intensity zone (km/h)
const SPEED_KMPH = {
  Ride: { Z1: 20, Z2: 27, Z3: 32, Z4: 36, Z5: 40, Race: 42 },
  Run:  { Z1: 7,  Z2: 10, Z3: 13, Z4: 15, Z5: 17, Race: 18 },
};

// Compass bearing by direction name
const BEARING = {
  N: 0, NE: 45, E: 90, SE: 135, S: 180, SW: 225, W: 270, NW: 315,
};

export function estimateDistanceKm(sport, zone, durationMin) {
  const speeds = SPEED_KMPH[sport] || SPEED_KMPH.Ride;
  const speed = speeds[zone] || 27;
  return Math.round((speed * durationMin) / 60 * 10) / 10;
}

/**
 * Move a point on the earth surface by a bearing and distance.
 * Returns { lat, lng }
 */
function geodesicOffset(lat, lng, bearingDeg, distanceKm) {
  const R = 6371;
  const d = distanceKm / R;
  const b = (bearingDeg * Math.PI) / 180;
  const lat1 = (lat * Math.PI) / 180;
  const lng1 = (lng * Math.PI) / 180;

  const lat2 = Math.asin(
    Math.sin(lat1) * Math.cos(d) + Math.cos(lat1) * Math.sin(d) * Math.cos(b)
  );
  const lng2 = lng1 + Math.atan2(
    Math.sin(b) * Math.sin(d) * Math.cos(lat1),
    Math.cos(d) - Math.sin(lat1) * Math.sin(lat2)
  );

  return {
    lat: (lat2 * 180) / Math.PI,
    lng: ((lng2 * 180) / Math.PI + 540) % 360 - 180,
  };
}

/**
 * Generate a circular loop that starts and ends at (startLat, startLng).
 * The circle is centered directly north of the start by one radius.
 */
function generateLoop(startLat, startLng, distanceKm, n = 64) {
  const radiusKm = distanceKm / (2 * Math.PI);
  const radiusDegLat = radiusKm / 111.32;
  const radiusDegLng = radiusKm / (111.32 * Math.cos((startLat * Math.PI) / 180));

  const centerLat = startLat + radiusDegLat;

  const pts = [];
  for (let i = 0; i <= n; i++) {
    const angle = (i / n) * 2 * Math.PI; // clockwise from south of center
    pts.push({
      lat: centerLat - radiusDegLat * Math.cos(angle),
      lng: startLng + radiusDegLng * Math.sin(angle),
    });
  }
  return pts;
}

/**
 * Generate an out-and-back route in a given compass direction.
 */
function generateOutAndBack(startLat, startLng, distanceKm, bearingDeg, n = 30) {
  const halfKm = distanceKm / 2;
  const pts = [];

  for (let i = 0; i <= n; i++) {
    const d = (i / n) * halfKm;
    pts.push(geodesicOffset(startLat, startLng, bearingDeg, d));
  }
  for (let i = n; i >= 0; i--) {
    const d = (i / n) * halfKm;
    pts.push(geodesicOffset(startLat, startLng, bearingDeg, d));
  }
  return pts;
}

/**
 * Generate a figure-8 route. Two loops joined at the start.
 */
function generateFigure8(startLat, startLng, distanceKm, n = 64) {
  const halfDist = distanceKm / 2;
  const loop1 = generateLoop(startLat, startLng, halfDist, n / 2);
  // Second loop goes south
  const radiusKm = halfDist / (2 * Math.PI);
  const radiusDegLat = radiusKm / 111.32;
  const radiusDegLng = radiusKm / (111.32 * Math.cos((startLat * Math.PI) / 180));
  const centerLat2 = startLat - radiusDegLat;
  const loop2 = [];
  for (let i = 0; i <= n / 2; i++) {
    const angle = (i / (n / 2)) * 2 * Math.PI;
    loop2.push({
      lat: centerLat2 + radiusDegLat * Math.cos(angle),
      lng: startLng + radiusDegLng * Math.sin(angle),
    });
  }
  return [...loop1, ...loop2];
}

/**
 * Add a synthetic elevation profile to route points.
 * profile: 'flat' | 'rolling' | 'hilly' | 'climb'
 */
function applyElevation(pts, profile, baseEle = 150) {
  const n = pts.length;
  return pts.map((p, i) => {
    const frac = i / Math.max(n - 1, 1);
    let ele = baseEle;
    if (profile === 'rolling') {
      ele = baseEle + 25 * Math.sin(frac * 4 * Math.PI);
    } else if (profile === 'hilly') {
      ele = baseEle + 80 * Math.sin(frac * 2 * Math.PI);
    } else if (profile === 'climb') {
      // Climb up and descend
      ele = frac < 0.6
        ? baseEle + (frac / 0.6) * 200
        : baseEle + 200 * (1 - (frac - 0.6) / 0.4);
    }
    return { ...p, ele: Math.round(ele) };
  });
}

/**
 * Build a GPX XML string from an array of { lat, lng, ele } points.
 */
function buildGpx(name, pts, distanceKm, sport) {
  const now = new Date().toISOString();
  const rtePts = pts
    .map(p => `    <rtept lat="${p.lat.toFixed(6)}" lon="${p.lng.toFixed(6)}"><ele>${p.ele ?? 100}</ele></rtept>`)
    .join('\n');

  return `<?xml version="1.0" encoding="UTF-8"?>
<gpx version="1.1" creator="CoachCenter APEX" xmlns="http://www.topografix.com/GPX/1/1">
  <metadata>
    <name>${name}</name>
    <desc>APEX generated ${sport} route — ${distanceKm} km</desc>
    <time>${now}</time>
  </metadata>
  <rte>
    <name>${name}</name>
    <desc>${distanceKm} km ${sport} route generated by APEX</desc>
${rtePts}
  </rte>
</gpx>`;
}

/**
 * Main export: generate and download a .gpx route file.
 *
 * @param {Object} opts
 * @param {number} opts.lat - Starting latitude
 * @param {number} opts.lng - Starting longitude
 * @param {string} opts.sport - 'Ride' | 'Run'
 * @param {string} opts.zone - 'Z1'..'Z5' | 'Race'
 * @param {number} opts.durationMin - Planned duration in minutes
 * @param {string} opts.shape - 'loop' | 'out-and-back' | 'figure8'
 * @param {string} opts.direction - 'N'|'NE'|'E'|'SE'|'S'|'SW'|'W'|'NW' (for out-and-back)
 * @param {string} opts.elevation - 'flat' | 'rolling' | 'hilly' | 'climb'
 * @param {string} [opts.label] - Optional route label
 */
export function generateAndDownloadGpx({
  lat, lng, sport = 'Ride', zone = 'Z2', durationMin = 90,
  shape = 'loop', direction = 'N', elevation = 'rolling', label = '',
}) {
  const distanceKm = estimateDistanceKm(sport, zone, durationMin);

  let pts;
  if (shape === 'out-and-back') {
    pts = generateOutAndBack(lat, lng, distanceKm, BEARING[direction] ?? 0);
  } else if (shape === 'figure8') {
    pts = generateFigure8(lat, lng, distanceKm);
  } else {
    pts = generateLoop(lat, lng, distanceKm);
  }

  pts = applyElevation(pts, elevation);

  const routeName = `APEX ${sport} ${zone} ${durationMin}min — ${distanceKm}km`;
  const gpx = buildGpx(routeName, pts, distanceKm, sport);

  const blob = new Blob([gpx], { type: 'application/gpx+xml' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  const dateStr = label || new Date().toISOString().split('T')[0];
  a.download = `apex-route-${sport.toLowerCase()}-${zone.toLowerCase()}-${dateStr}.gpx`;
  a.click();
  URL.revokeObjectURL(url);

  return { distanceKm, routeName };
}
